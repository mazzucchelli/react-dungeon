/*

reference

+--+--+--+--+--+
|  |  |  |  |  |
+--+--+--+--+--+
|  |  |  |  |  |
+--+--+--+--+--+
|  |  |  |  |  |
+--+--+--+--+--+
|  |  |  |  |  |
+--+--+--+--+--+

*/

const playerView = {
  front: (x, y) => {
    /*
      +---+---+---+
      |   |   |   |
      +---+---+---+
      | x | x | x |
      +---+---+---+
      |   | P |   |
      +---+---+---+
    */
    return [`${x + 1} - ${y - 1}`, `${x + 1} - ${y}`, `${x + 1} - ${y + 1}`];
  },
  round: (x, y) => {
    /*
      +---+---+---+
      | x | x | x |
      +---+---+---+
      | x | P | x |
      +---+---+---+
      | x | x | x |
      +---+---+---+
    */
    return [
      `${x - 1} - ${y - 1}`,
      `${x - 1} - ${y}`,
      `${x - 1} - ${y + 1}`,
      `${x} - ${y - 1}`,
      `${x} - ${y + 1}`,
      `${x + 1} - ${y - 1}`,
      `${x + 1} - ${y}`,
      `${x + 1} - ${y + 1}`
    ];
  },
  front_round: (x, y) => {
    /*
      +---+---+---+
      |   |   |   |
      +---+---+---+
      | x | x | x |
      +---+---+---+
      | x | P | x |
      +---+---+---+
    */
    return [
      `${x} - ${y - 1}`,
      `${x} - ${y + 1}`,
      `${x + 1} - ${y - 1}`,
      `${x + 1} - ${y}`,
      `${x + 1} - ${y + 1}`];
  },
  perimeter: (x, y) => {
    /*
      +---+---+---+
      |   | x |   |
      +---+---+---+
      | x | P | x |
      +---+---+---+
      |   | x |   |
      +---+---+---+
    */
    return [
      `${x - 1} - ${y}`,
      `${x} - ${y - 1}`,
      `${x} - ${y + 1}`,
      `${x + 1} - ${y}`
    ];
  },
  front_perimeter: (x, y) => {
    /*
      +---+---+---+
      |   |   |   |
      +---+---+---+
      |   | x |   |
      +---+---+---+
      | x | P | x |
      +---+---+---+
    */
    return [`${x} - ${y - 1}`, `${x} - ${y + 1}`, `${x + 1} - ${y}`];
  }
};

export default playerView;
